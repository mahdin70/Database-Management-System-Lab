CREATE TABLE ACCOUNTPROPERTY(
    ID INT PRIMARY KEY,
    NAME VARCHAR2(20),
    PROFITRATE NUMERIC(10,2),
    GRACEPERIOD INT
);

CREATE TABLE ACCOUNT(
    ID INT PRIMARY KEY,
    NAME VARCHAR2(20),
    ACCCODE INT,
    OPENINGDATE DATE,
    LASTDATEINTEREST DATE,

    CONSTRAINT FK_ACCOUNT_ACCOUNTPROPERTY FOREIGN KEY(ACCCODE) REFERENCES ACCOUNTPROPERTY(ID)
);

CREATE TABLE TRANSACTION(
    TID INT PRIMARY KEY,
    ACCNO INT,
    AMOUNT NUMERIC(10,2),
    TRANSACTIONDATE DATE,

    CONSTRAINT FK_TRANSACTION_ACCNO FOREIGN KEY(ACCNO) REFERENCES ACCOUNT(ID)
);

CREATE TABLE BALANCE(
    ACCNO INT PRIMARY KEY,
    PRINCIPALAMOUNT BALANCE NUMERIC(10,2),
    PRINCIPALAMOUNT PROFIT NUMERIC(10,2),

    CONSTRAINT FK_BALANCE_ACCNO FOREIGN KEY(ACCNO) REFERENCES ACCOUNT(ID)
);


